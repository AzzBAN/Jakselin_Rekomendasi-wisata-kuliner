     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        try{
            $user = Auth::user();
            $this->validate($request, [
                'nama' => "required",
                'username' => "required",
                'nohp' => "required"
            ]);

            User::where('id', $user->id)->update([
                "nama" => $request->nama,
                "username" => $request->username,
                "nohp" => $request->nohp
            ]);

            return redirect('/profile');
        }catch(\Exception $e){
            dd($e);
            return redirect('/profile');
        }
    }

